            ['9030', 'Agus', 'L', '2025-12-10'],
            ['9031', 'Desi', 'P', '2025-06-19'],
            ['9032', 'ezzy', 'L', '06/11/2025'],
            ['9033', 'Nazil', 'L', '2025-12-10'],
            ['9034', '-', 'P', '0000-00-00'],
        ];

        foreach ($dataMaster as $row) {
            $kode = trim($row[0]);
            $nama = trim($row[1]);
            $jkRaw = trim($row[2]);
            $tglRaw = trim($row[3]);

            // 1. Logika Nama Kosong
            if ($nama === '' || $nama === null) {
                $nama = '-';
            }

            // 2. Logika Jenis Kelamin (Database: 0=Laki, 1=Perempuan)
            $jk = (strtoupper($jkRaw) === 'P') ? 1 : 0;

            // 3. Logika Parsing Tanggal (Menangani berbagai format Excel)
            $tglMasuk = null;
            try {
                if (empty($tglRaw) || $tglRaw === '0000-00-00') {
                    // Jika tanggal nol/kosong, set ke NULL atau Hari Ini (sesuaikan kebutuhan)
                    $tglMasuk = now();
                } elseif (str_contains($tglRaw, '/')) {
                    // Menangani format DD/MM/YYYY (contoh: 15/10/2025)
                    $tglMasuk = Carbon::createFromFormat('d/m/Y', $tglRaw);
                } else {
                    // Menangani format YYYY-MM-DD
                    $tglMasuk = Carbon::parse($tglRaw);
                }
            } catch (\Exception $e) {
                // Jika error parsing, default ke hari ini
                $tglMasuk = now();
            }

            // 4. Update atau Create Data
            Pegawai::updateOrCreate(
                ['kode_pegawai' => $kode],
                [
                    'nama_pegawai' => $nama,
                    'jenis_kelamin_pegawai' => $jk,
                    'tanggal_masuk' => $tglMasuk,

                    // Default values
                    'alamat' => 'Malang',
                    'no_telepon_pegawai' => '0812000000',
                ]
            );
        }
